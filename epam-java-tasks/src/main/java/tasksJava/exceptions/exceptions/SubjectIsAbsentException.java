package tasksJava.exceptions.exceptions;

public class SubjectIsAbsentException extends Exception {

	
	private static final long serialVersionUID = 3473226474077399062L;

	public SubjectIsAbsentException() {
		super();
	}

	public SubjectIsAbsentException(String message, Throwable cause) {
		super(message, cause);
	}

	public SubjectIsAbsentException(String message) {
		super(message);
	}

	public SubjectIsAbsentException(Throwable cause) {
		super(cause);
	}
	
	

}
